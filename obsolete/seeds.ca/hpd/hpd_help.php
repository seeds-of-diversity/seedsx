<P><B>Seed Search:</B></P>
<P>Use this to search for a species or cultivar.</P>
</P>
<OL>
<LI>Select <B>Seeds</B>.
<LI>Enter <B>one or more words</B>, separated by spaces, into the search box:
    <UL>
        <LI>Entering more words will help narrow the search<BR>(eg. <CODE>tomato sunset allegheny</CODE>)
        <LI>You may enter parts of words if you don't know how to spell a word (eg. enter "all" if you're not sure how to spell "allegheny")
        <LI>Searching is case-insensitive.  Capitalization does not matter.
    </UL>
<LI>Click <B>Go</B> to see the results of the search.
</OL>
<P><B>Catalog Search:</B></P>
<P>Use this to search for historical seed catalogues which have been reproduced by Seeds of Diversity.</P>
<OL>
<LI>Select <B>Catalogues</B>.
<LI>Enter <B>one or more words</B>, separated by spaces, into the search box:
    <UL>
        <LI>Enter words from the title, publisher, or place of publication<BR>(eg. <CODE>keith toronto</CODE>)
        <LI>Entering more words will help narrow the search
        <LI>You may enter parts of words if you are unsure of spelling
        <LI>Searching is case-insensitive.  Capitalization does not matter.
    </UL>
<LI>Click <B>Go</B> to see the results of the search.
</OL>
<P><B>Search by Seed Availability:</B></P>
<P>Use this to search for cultivars according to how common or rare they are.  Note that source information is not available for all cultivars in the database.  This means that some cultivars can
only be found by using the regular seed search, or by browsing.</P>
<OL>
<LI>Select <B>Search by Seed Availability</B>.
<LI>On the next page you may select the search criteria.  For any criteria, you may select <B>no restriction</B> if that criteria should <em>not</em> apply.
<LI>If too many records are found, you may wish to search again using stricter criteria, in order
to limit the number of results.
<LI>Click <B>Go</B> to see the results of the search.
</OL>
</TD></TR></TABLE>
