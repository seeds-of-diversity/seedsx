<H3>Flowers</H3>
<P>
Plants and pollinating insects need each other.
</P>
<P>
Pollen is the yellow, white or brown powder produced at the center of most flowers.  You can see it by
gently rubbing a finger over the upright structures inside an open flower.<P>
<P>
Pollen serves two purposes for plants: it stimulates the creation of fruit and seeds, and it contains the plants'
genetic code.  When pollen from one plant <I>cross-pollinates</I> the flowers of another plant, the resulting seeds
contain a combination of the characteristics of both plants. When pollen moves freely between plants, genetic
characteristics are distributed, making the whole plant population healthier.
</P>
<P>
Pollen is also important for many insects. It is high in protein (between seven and 35%), starch, and vitamins,
and it is a vital food for pollinators and their larvae.
</P>
<P>
Plants and insects have co-evolved in a <I>symbiotic relationship</I>.  This means that neither can exist without
the other. Plants depend on insects to carry their pollen from flower to flower, and insects depend on pollen and
flower nectar for food.
</P>

